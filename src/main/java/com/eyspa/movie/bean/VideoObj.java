package com.eyspa.movie.bean;

public class VideoObj {

    private int pai;
    private String name;
    private String href;
    private String pn;

    public int getPai()
    {
        return this.pai;
    }

    public void setPai(int pai)
    {
        this.pai = pai;
    }

    public String getName()
    {
        return this.name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getHref()
    {
        return this.href;
    }

    public void setHref(String href)
    {
        this.href = href;
    }

    public String getPn()
    {
        return this.pn;
    }

    public void setPn(String pn)
    {
        this.pn = pn;
    }
}
